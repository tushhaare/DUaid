  document.getElementById('login-form').addEventListener('submit', (event) => {
      event.preventDefault();
  
      const phone = document.getElementById('phone').value.trim();
      const email = document.getElementById('email').value.trim();
  
      if (!phone || phone.length < 10 || !email.includes('@')) {
          alert('Please enter a valid phone number and email address.');
          return;
      }
  
      // Check if the phone and email match an entry in the JSON
      const user = authorizedUsers.find(user => 
          user.Phone.toString() === phone && user.Email === email
      );
  
      if (user) {
          if (user.Granted) {
              alert('Access Granted! Redirecting...');
              document.getElementById('login-section').style.display = 'none';
              document.getElementById('webinar-section').style.display = 'block';
          } else {
              alert('Access Denied! Your access is not granted.');
          }
      } else {
          alert('Access Denied! Details not found.');
      }
  });
