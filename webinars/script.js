// Form Submit Listener
document.getElementById('login-form').addEventListener('submit', (event) => {
    event.preventDefault();
  
    // Get user input
    const phoneLast4 = parseInt(document.getElementById('phoneLast4').value.trim());
    const email = document.getElementById('email').value.trim();
  
    // Check if the inputs are valid
    if (isNaN(phoneLast4) || phoneLast4.toString().length !== 4 || !email.includes('@')) {
      alert('Please enter valid last 4 digits of your phone number and email.');
      return;
    }
  
    // Find matching user in JSON data
    const user = authorizedUsers.find(
      (u) => u.PhoneLast4 === phoneLast4 && u.Email.toLowerCase() === email.toLowerCase()
    );
  
    if (user && user.Granted) {
      alert('Access Granted! Redirecting...');
      // Hide login section and show webinar section
      document.getElementById('login-section').style.display = 'none';
      document.getElementById('webinar-section').style.display = 'block';
    } else {
      alert('Access Denied! Details do not match.');
    }
  });
